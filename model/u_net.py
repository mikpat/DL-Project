def gcn(inputs, output_filters, k):

    x_1 = Conv2D(output_filters, (k, 1), padding='same')(inputs)
    x_1 = Conv2D(output_filters, (1, k), padding='same')(x_1)
    x_2 = Conv2D(output_filters, (k, 1), padding='same')(inputs)
    x_2 = Conv2D(output_filters, (1, k), padding='same')(x_2)

    return Add()([x_1, x_2])


